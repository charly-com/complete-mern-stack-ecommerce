import mongoose,{Schema} from "mongoose";


const usersSchema = Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    isAdmin: {
        type: Boolean,
        required: true,
        default: false
    }
}, {
    timestamps: true
}) 

const aUser = mongoose.model("Users", usersSchema)

export default aUser;